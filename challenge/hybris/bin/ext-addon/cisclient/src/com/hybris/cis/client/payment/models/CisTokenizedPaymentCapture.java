package com.hybris.cis.client.payment.models;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;


/**
 * A payment request to capture an amount of money based on tokens generated by a previous authorization.
 */
@XmlRootElement(name = "tokenizedPaymentCapture")
@XmlAccessorType(XmlAccessType.FIELD)
public class CisTokenizedPaymentCapture extends CisPaymentRequest
{
	/** specifies if a capture was complete or not complete. */
	@XmlElement(name = "completeType")
	private CisCaptureCompleteType completeType;

	/** the request id of the authorization to capture on. */
	@XmlElement(name = "authorizationRequestId")
	private String authorizationRequestId;

	/** the authorization request token used. */
	@XmlElement(name = "authorizationRequestToken")
	private String authorizationRequestToken;

	public CisCaptureCompleteType getCompleteType()
	{
		return this.completeType;
	}

	public void setCompleteType(final CisCaptureCompleteType completeType)
	{
		this.completeType = completeType;
	}

	public String getAuthorizationRequestId()
	{
		return this.authorizationRequestId;
	}

	public void setAuthorizationRequestId(final String authorizationRequestId)
	{
		this.authorizationRequestId = authorizationRequestId;
	}

	public String getAuthorizationRequestToken()
	{
		return this.authorizationRequestToken;
	}

	public void setAuthorizationRequestToken(final String authorizationRequestToken)
	{
		this.authorizationRequestToken = authorizationRequestToken;
	}

	@Override
	public String toString()
	{
		return "CisTokenizedPaymentCapture [amount=" + this.getAmount() + ", currency=" + this.getCurrency() + ", completeType="
				+ this.completeType + ", authorizationRequestId=" + this.authorizationRequestId + ", authorizationRequestToken="
				+ this.authorizationRequestToken + "]";
	}
}
