package de.hybris.platform.odata2services.odata.schema;

/**
 * Abstraction of a generator for a {@link org.apache.olingo.odata2.api.edm.provider.Schema} element.
 *
 * @param <R> target type of the schema element to be generated by this generator.
 * @param <P> source type of the object, from which the schema element can be generated.
 */
public interface SchemaElementGenerator<R, P>
{
	/**
	 * Generates the schema element whose type is defined by T.
	 *
	 * @param object the object that is being converted
	 * @return R the schema element
	 */
	R generate(P object);
}
